#include <iostream>
using namespace std;

int main() {
    int A, B;

    // 두 자연수 입력
    cin >> A >> B;

    // 결과를 바로 출력
    cout << A + B << endl; // A+B
    cout << A - B << endl; // A-B
    cout << A * B << endl; // A*B
    cout << A / B << endl; // A/B(몫)
    cout << A % B << endl; // A%B(나머지)

    return 0;
}